// =      x = y   x = y
// +=      x += y   x = x + y
// -=      x -= y   x = x - y
// *=      x *= y   x = x * y
// /=      x /= y   x = x / y
// %=      x %= y   x = x % y

var a = 15;
var b = 10;

var c = a + b  // Soma
var d = a - b  // Subtração
var e = a * b  // Multiplicação
var f = a / b  // Divisão
var g = a % b  // Módulo = O resto da divisão de um número. 15/10 resta 5
var h = a++    // Incremento = a recebe ele mesmo mais 1
var i = a--    // Decremento = a recebe ele mesmo menos 1

console.log(c)
console.log(d)
console.log(e)
console.log(f)
console.log(g)
console.log(h)
console.log(i)